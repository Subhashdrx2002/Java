public class Typecasting {
    public static void main(String[]args){
        int a = 45;
        double b;
        b=a;
        System.out.println(b);

        double num1 = 35.4;
        int num2 = 30;
        num2=(int)num1;
        System.out.println(num2);

        int n1 = 12;
        int n2 = 5;
        float res=n1/n2;
        System.out.println(res);
    
    }
    
}